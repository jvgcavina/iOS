import Foundation

func arithmeticOperatorsMethod() {
    // Arithmetic Operators
    var number1 = 5.0
    var number2 = 2.0
    var result: Double

    // Plus
    result = number1 + number2
    print(result)

    // Minus
    result = number1 - number2
    print(result)

    // Times
    result = number1 * number2
    print(result)

    // Divide
    result = number1 / number2
    print(result)
}
